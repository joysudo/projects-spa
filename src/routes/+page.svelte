                <h3>Middle School Programs @ Pi515</h3>
                <p>I joined Pi515 when I was feeling tired of government. Chatting with legislators feels tokenistic after your third day of it. Pi515 was a partner in a mentorship I did with John Deere, shadowing software engineers and developing games in Python. I sent a cold email from the back of my sixth period classroom and begged to be let in. </p>
                <a href="https://replit.com/@byjs/MazeGame">Example goofy game</a>
                <p>It worked! I joined when the 2023 middle school initiative was just in its initial planning phase, and helped take it from concepts of a plan to execution. Accessibility for a program like this means maintaining a sufficient student:mentor ratio and providing students with transportation home, which involved calling and coordinating. I developed curriculum and pacing. We launched in 3 underserved middle schools in the DMPS district, teaching ~50 kids block coding for 4 months.</p>
